#include<unistd.h>

void	ft_is_negative(int n)
{
	int	i;
	int	o;

	i = 'N';
	o = 'P';
	if (n > 0 || n == '\0')
	{
		n = o;
		write(1, &n, 1);
	}
	else
	{
		n = i;
		write(1, &n, 1);
	}
}
